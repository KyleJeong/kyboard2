package com.young2000.kyboard2;

public class WordWithFreqMax {
    private String word;
    private int freqMax;

    public WordWithFreqMax(String word, int freqMax) {
        this.word = word;
        this.freqMax = freqMax;
    }

    public String getWord() {
        return word;
    }

    public int getFreqMax() {
        return freqMax;
    }
}
